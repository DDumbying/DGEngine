#define GLYPH_COUNT (int)(sizeof(GLYPHS) / sizeof(GLYPHS[0]))

const char *const *font_get_glyph(char c) {
    char up = (char)toupper((unsigned char)c);
    for (int i = 0; i < GLYPH_COUNT; i++) {
        if (GLYPHS[i].c == up) return GLYPHS[i].rows;
    }
    return NULL;
}
